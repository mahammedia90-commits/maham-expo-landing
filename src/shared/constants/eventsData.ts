// Helper to generate investor-specific descriptions
export function getInvestorEvents(): MerchantEvent[] {
  return saudiEventsDatabase.map((event) => ({
    ...event,
    id: `inv-${event.id}`,
    description: getInvestorDescription(event),
    descriptionEn: getInvestorDescriptionEn(event),
    availableUnits: Math.max(5, Math.floor(event.availableUnits * 0.6)),
    totalUnits: Math.floor(event.totalUnits * 0.4),
  }));
}

function getInvestorDescription(event: MerchantEvent): string {
  const templates: Record<string, string> = {
    entertainment: `${event.name} - فرصة استثمارية مميزة لتأجير المساحات والقاعات والمحلات التجارية. طلب عالي على المواقع المميزة.`,
    cultural: `${event.name} - فرصة لعرض مساحاتك للتجار والفنانين. احتياج كبير لمساحات العرض والمتاجر.`,
    technology: `${event.name} - فرصة لتأجير مساحات المعارض والقاعات بأسعار مميزة لشركات التقنية والابتكار.`,
    conference: `${event.name} - فرصة تأجير قاعات مؤتمرات ومساحات عرض ومكاتب مؤقتة للشركات المشاركة.`,
    exhibition: `${event.name} - فرصة تأجير مساحات عرض واسعة ومستودعات ومحلات تجارية للعارضين.`,
    sports: `${event.name} - فرصة لتأجير المساحات والمحلات التجارية والمطاعم للجماهير والزوار.`,
  };
  return templates[event.type] || event.description;
}

function getInvestorDescriptionEn(event: MerchantEvent): string {
  const templates: Record<string, string> = {
    entertainment: `${event.nameEn || event.name} - Prime investment opportunity for leasing spaces, halls, and shops. High demand for premium locations.`,
    cultural: `${event.nameEn || event.name} - Opportunity to showcase your spaces to merchants and artists. High demand for exhibition spaces and shops.`,
    technology: `${event.nameEn || event.name} - Opportunity to lease exhibition spaces and halls at premium rates for tech and innovation companies.`,
    conference: `${event.nameEn || event.name} - Opportunity to lease conference halls, exhibition spaces, and temporary offices for participating companies.`,
    exhibition: `${event.nameEn || event.name} - Opportunity to lease large exhibition spaces, warehouses, and retail shops for exhibitors.`,
    sports: `${event.nameEn || event.name} - Opportunity to lease spaces, retail shops, and restaurants for fans and visitors.`,
  };
  return templates[event.type] || (event.descriptionEn || event.description);
}
